void shellHelp(void) {
    printf("Bem vindo ao Mini Shell!\n");
    printf("This shell supports the following commands:\n");
    printf("1. help - Display this help message\n");
    printf("2. exit - Exit the shell\n");
    printf("3. cd <directory> - Change the current directory\n");
    printf("4. pwd - Print the current working directory\n");
    printf("5. ls - List files in the current directory\n");
    printf("6. cat <file> - Display the contents of a file\n");
    printf("7. echo <text> - Print text to the console\n");
}